import { useEffect, useState } from "react";

function ThemeSwitch() {
  const initialTheme = localStorage.theme === "dark" || 
    (!("theme" in localStorage) && window.matchMedia("(prefers-color-scheme: dark)").matches) 
    ? "dark" 
    : "light";

  const [theme, setTheme] = useState(initialTheme);

  useEffect(() => {
    document.documentElement.classList.toggle("dark", theme === "dark");
  }, [theme]);

  const toggleTheme = () => {
    const newTheme = theme === "dark" ? "light" : "dark";
    setTheme(newTheme);
    localStorage.setItem("theme", newTheme);
  };

  return (
    <button onClick={toggleTheme} className="cursor-pointer">
        {theme === "dark" ? (
          <svg width="35" height="35" viewBox="0 0 23 23" fill="none">
            <path d="M7.24414 0C5.0953 0.927588 3.26462 2.46332 1.9775 4.41812C0.690379 6.37293 0.00300213 8.66146 0 11.002C0 14.1846 1.26428 17.2368 3.51472 19.4872C5.76516 21.7377 8.8174 23.002 12 23.002C14.2971 22.9997 16.5452 22.3381 18.4775 21.0959C20.4097 19.8537 21.9448 18.083 22.9004 15.9941C21.3992 16.6479 19.78 16.9876 18.1426 16.9922C14.96 16.9922 11.9077 15.7279 9.6573 13.4775C7.40686 11.227 6.14258 8.17479 6.14258 4.99219C6.14692 3.26832 6.52264 1.56562 7.24414 0Z" fill="black"/>
          </svg>
        ) : (
          <svg width="35" height="35" viewBox="0 0 45 45" fill="none">
            <path d="M22.5001 31.8575C27.668 31.8575 31.8575 27.668 31.8575 22.5C31.8575 17.332 27.668 13.1425 22.5001 13.1425C17.3321 13.1425 13.1426 17.332 13.1426 22.5C13.1426 27.668 17.3321 31.8575 22.5001 31.8575Z" fill="black"/>
            <path d="M22.5 0C21.8665 0 21.2452 0.0439519 20.625 0.0952144V9.375H24.375V0.0952148C23.7548 0.0439527 23.1335 0 22.5 0ZM6.5901 6.5901C6.14217 7.03803 5.73388 7.50847 5.3316 7.98325L11.8934 14.545L14.545 11.8934L7.98325 5.3316C7.50847 5.73388 7.03803 6.14217 6.5901 6.5901ZM0 22.5C0 23.1335 0.0439519 23.7548 0.0952144 24.375H9.375V20.625H0.0952148C0.0439527 21.2452 0 21.8665 0 22.5ZM6.5901 38.4099C7.03803 38.8578 7.50847 39.2661 7.98325 39.6684L14.545 33.1066L11.8934 30.455L5.3316 37.0168C5.73388 37.4915 6.14217 37.962 6.5901 38.4099ZM22.5 45C23.1335 45 23.7548 44.9561 24.375 44.9048V35.625H20.625V44.9048C21.2452 44.956 21.8665 45 22.5 45ZM38.4099 38.4099C38.8578 37.962 39.2661 37.4915 39.6684 37.0168L33.1066 30.455L30.455 33.1066L37.0168 39.6684C37.4915 39.2661 37.962 38.8578 38.4099 38.4099ZM45 22.5C45 21.8665 44.9561 21.2452 44.9048 20.625H35.625V24.375H44.9048C44.956 23.7548 45 23.1335 45 22.5ZM38.4099 6.5901C37.962 6.14217 37.4915 5.73388 37.0168 5.3316L30.455 11.8934L33.1066 14.545L39.6684 7.98325C39.2661 7.50847 38.8578 7.03803 38.4099 6.5901Z" fill="black"/>
          </svg>
        )}
    </button>
  );
}

export default ThemeSwitch;